/*
BOJ 16236번: 아기 상어

시간 제한: 2 초
메모리 제한: 512 MB

문제
N×N 크기의 공간에 물고기 M마리와 아기 상어 1마리가 있다. 공간은 1×1 크기의 정사각형 칸으로 나누어져 있다. 한 칸에는 물고기가 최대 1마리 존재한다.
아기 상어와 물고기는 모두 크기를 가지고 있고, 이 크기는 자연수이다. 가장 처음에 아기 상어의 크기는 2이고, 아기 상어는 1초에 상하좌우로 인접한 한 칸씩 이동한다.
아기 상어는 자신의 크기보다 큰 물고기가 있는 칸은 지나갈 수 없고, 나머지 칸은 모두 지나갈 수 있다. 아기 상어는 자신의 크기보다 작은 물고기만 먹을 수 있다. 따라서, 크기가 같은 물고기는 먹을 수 없지만, 그 물고기가 있는 칸은 지나갈 수 있다.
아기 상어가 어디로 이동할지 결정하는 방법은 아래와 같다.

더 이상 먹을 수 있는 물고기가 공간에 없다면 아기 상어는 엄마 상어에게 도움을 요청한다.
먹을 수 있는 물고기가 1마리라면, 그 물고기를 먹으러 간다.
먹을 수 있는 물고기가 1마리보다 많다면, 거리가 가장 가까운 물고기를 먹으러 간다.
	거리는 아기 상어가 있는 칸에서 물고기가 있는 칸으로 이동할 때, 지나야하는 칸의 개수의 최솟값이다.
	거리가 가까운 물고기가 많다면, 가장 위에 있는 물고기, 그러한 물고기가 여러마리라면, 가장 왼쪽에 있는 물고기를 먹는다. 
아기 상어의 이동은 1초 걸리고, 물고기를 먹는데 걸리는 시간은 없다고 가정한다. 즉, 아기 상어가 먹을 수 있는 물고기가 있는 칸으로 이동했다면, 이동과 동시에 물고기를 먹는다. 물고기를 먹으면, 그 칸은 빈 칸이 된다.

아기 상어는 자신의 크기와 같은 수의 물고기를 먹을 때 마다 크기가 1 증가한다. 예를 들어, 크기가 2인 아기 상어는 물고기를 2마리 먹으면 크기가 3이 된다.
공간의 상태가 주어졌을 때, 아기 상어가 몇 초 동안 엄마 상어에게 도움을 요청하지 않고 물고기를 잡아먹을 수 있는지 구하는 프로그램을 작성하시오.

입력
첫째 줄에 공간의 크기 N(2 ≤ N ≤ 20)이 주어진다.
둘째 줄부터 N개의 줄에 공간의 상태가 주어진다. 공간의 상태는 0, 1, 2, 3, 4, 5, 6, 9로 이루어져 있고, 아래와 같은 의미를 가진다.

0: 빈 칸
1, 2, 3, 4, 5, 6: 칸에 있는 물고기의 크기
9: 아기 상어의 위치
아기 상어는 공간에 한 마리 있다.

출력
첫째 줄에 아기 상어가 엄마 상어에게 도움을 요청하지 않고 물고기를 잡아먹을 수 있는 시간을 출력한다. 
*/

#include <bits/stdc++.h>

using namespace std;

typedef long long ll;
typedef unsigned long long ull;

#define MAX 20

int n;
int arr[20][20];

bool visited[20][20];
int dx[4] = { 0, -1, 0, 1 };
int dy[4] = { -1, 0, 1, 0 };

int shark_x,shark_y, shark_weight, ate;
int ans;

typedef struct _node {
	int x;
	int y;
	int move;
} Node;

struct compare {
	bool operator()(Node a, Node b) {
		if(a.move == b.move) {
			if(a.y == b.y) {
				return a.x > b.x;
			}
			else {
				return a.y > b.y;
			}
		}
		else {
			return a.move > b.move;
		}
	}
};

int bfs(int start_x, int start_y) {
	queue<Node> q;
	priority_queue<Node, vector<Node>, compare> pq;
	visited[start_y][start_x] = true;
	Node node = { start_x, start_y, 0 };
	q.push(node);
	
	while(!q.empty()) {
		int x = q.front().x;
		int y = q.front().y;
		int move = q.front().move;
		q.pop();
		
		for(int i=0; i<4; i++) {
			int new_x = x + dx[i];
			int new_y = y + dy[i];
						
			if(0 <= new_x && new_x < n && 0 <= new_y && new_y < n) {
				// can move
				if(!visited[new_y][new_x] && shark_weight >= arr[new_y][new_x]) {
					Node node = { new_x, new_y, move + 1 };
					visited[new_y][new_x] = true;
					q.push(node);
					
					// if fish and can eat
					if(shark_weight > arr[new_y][new_x] && 1 <= arr[new_y][new_x] && arr[new_y][new_x] <= 6) {
						pq.push(node);
					}
				}
			}
		}
	}
	
	// eat most top left fish
	if(!pq.empty()) {
		ate++;
		if(ate == shark_weight) {
			ate = 0;
			shark_weight++;
		}
						
		shark_x = pq.top().x; shark_y = pq.top().y;
		arr[shark_y][shark_x] = 0;

		return pq.top().move;
	}
	// if no fish no eat
	else {
		return 0;
	}
}

int main(void) {
	ios_base::sync_with_stdio(false);
	cin.tie(NULL);
	cout.tie(NULL);
	
	cin >> n;
	for(int i=0; i<n; i++) {
		for(int j=0; j<n; j++) {
			int num;
			cin >> num;
			
			if(num == 9) {
				shark_x = j; shark_y = i;
			}
			else {
				arr[i][j] = num;
			}
		}
	}
	
	shark_weight = 2;
	while(true) {
		int cnt = bfs(shark_x, shark_y);
		if(cnt == 0) {
			break;
		}
		else {
			memset(visited, false, sizeof(visited));
			ans += cnt;
		}
	}
	
	cout << ans;
	return 0;
}

