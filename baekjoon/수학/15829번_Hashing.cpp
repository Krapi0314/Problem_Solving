using namespace std;

typedef long long ll;
typedef unsigned long long ull;

#define MOD 1234567891

int l;
string str;

int main(void) {
	ios_base::sync_with_stdio(false);
	cin.tie(NULL);
	cout.tie(NULL);
	
	cin >> l;
	cin >> str;
	
	ull ans = 0, r = 1;
	for(int i=0; i<l; i++) {
		ans = (ans + (str[i]-'a'+1) * r) % MOD;
		r = (r * 31) % MOD;
	}
		
	cout << ans;	
	return 0;
}

