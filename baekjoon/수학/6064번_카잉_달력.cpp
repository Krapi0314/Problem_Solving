#include <bits/stdc++.h>

using namespace std;

typedef long long ll;
typedef unsigned long long ull;

int t;
int n, m;
int x, y;
int k;
bool flag;

int gcd(int a, int b) {
	if(b == 0) return a;
	else return gcd(b, a%b);
}

int main(void) {
	ios_base::sync_with_stdio(false);
	cin.tie(NULL);
	cout.tie(NULL);
	
	cin >> t;
	while(t--) {		
		cin >> m >> n >> x >> y;
		
		int lcm = m * n / gcd(m,n);
		flag = false; int i = 0;
		while(true) {
			k = m*(i++)+x;
			if(k > lcm) break;
			else if((k-y) % n == 0) {
				flag = true;
				break;
			}
		}
		
		if(flag) cout << k << "\n";
		else cout << "-1\n";
	}
	return 0;
}

