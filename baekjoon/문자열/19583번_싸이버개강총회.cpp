#include <bits/stdc++.h>

using namespace std;

typedef long long ll;
typedef unsigned long long ull;

vector<string> before;
set<string> after;
string s,e,q;
string times, nickname;
int cnt;

int main(void) {
	ios_base::sync_with_stdio(false);
	cin.tie(NULL);
	cout.tie(NULL);
	
	cin >> s >> e >> q;
	while(true) {
		cin >> times >> nickname;
		
		if(cin.eof()) {
			break;
		}
		
		if(times <= s) {
			before.push_back(nickname);
		}
		else if(times >= e && times <= q) {
			after.insert(nickname);
		}
	}
	
	sort(before.begin(), before.end());
	before.erase(unique(before.begin(), before.end()),before.end());
	
	for(int i=0; i<before.size(); i++) {
		if(after.count(before[i])) {
			cnt++;
		}
	}
	
	cout << cnt;
}

