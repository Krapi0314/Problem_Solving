#include <bits/stdc++.h>

using namespace std;

typedef long long ll;
typedef unsigned long long ull;

string s;
string new_s;
string k;
bool flag;

int main(void) {
	ios_base::sync_with_stdio(false);
	cin.tie(NULL);
	cout.tie(NULL);

	cin >> s;
	cin >> k;
	
	for(int i=0; i<(int)s.size(); i++) {
		if(!(s[i] >= '0' && s[i] <= '9')) {
			new_s += s[i];
		}
	}
	
	for(int i=0; i<(int)new_s.size(); i++) {
		if(new_s.substr(i, (int)k.size()) == k) {
			flag = true;
			break;
		}
	}
	
	if(flag) {
		cout << "1";
	}
	else {
		cout << "0";
	}
	
	return 0;
}

