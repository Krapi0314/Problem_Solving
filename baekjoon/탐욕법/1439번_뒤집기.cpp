using namespace std;

typedef long long ll;
typedef unsigned long long ull;

string s;
int cnt0, cnt1;

int main(void) {
	ios_base::sync_with_stdio(false);
	cin.tie(NULL);
	cout.tie(NULL);
	
	cin >> s;
	
	s.erase(unique(s.begin(),s.end()),s.end());
	
	for(int i=0; i<(int)s.size(); i++) {
		if(s[i] == '0') cnt0++;
		else cnt1++;
	}
	
	cout << min(cnt0,cnt1);
}

