#include <bits/stdc++.h>

using namespace std;

typedef long long ll;
typedef unsigned long long ull;

int n;

int main(void) {
	ios_base::sync_with_stdio(false);
	cin.tie(NULL);
	cout.tie(NULL);

	cin >> n;
	
	if(n % 2 == 0) {
		for(int i=0; i<n; i++) {
			if(i % 2 == 0) {
				cout << "1 ";
			}
			else {
				cout << "2 ";
			}
		}
	}
	else {
		for(int i=0; i<n-1; i++) {
			if(i % 2 == 0) {
				cout << "1 ";
			}
			else {
				cout << "2 ";
			}
		}
		cout << "3";
	}
	
	return 0;
}

