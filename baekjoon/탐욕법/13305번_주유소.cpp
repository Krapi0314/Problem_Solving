//#define _CRT_SECURE_NO_WARNINGS

#include <bits/stdc++.h>

using namespace std;

typedef unsigned long long ull;
int n;
ull num, ans,Min;
vector<ull> dist;
vector<ull> price;

int main(void)
{
	ios_base::sync_with_stdio(false);
	cin.tie(NULL);
	cout.tie(NULL);

	cin >> n;
	for (int i = 0; i < n - 1; i++)
	{
		cin >> num;
		dist.push_back(num);
	}
	for (int i = 0; i < n; i++)
	{
		cin >> num;
		price.push_back(num);
	}

	Min = price[0];
	for (int i = 0; i < n - 1; i++)
	{
		if (price[i] >= Min)
			ans += Min * dist[i];
		else
		{
			Min = price[i];
			ans += Min * dist[i];
		}
	}
	
	cout << ans;
}

