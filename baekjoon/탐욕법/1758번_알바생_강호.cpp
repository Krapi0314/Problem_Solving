#include <bits/stdc++.h>

using namespace std;

typedef long long ll;
typedef unsigned long long ull;

ull ans;
vector<int> v;
int num,n;

int main(void) {
	ios_base::sync_with_stdio(false);
	cin.tie(NULL);
	cout.tie(NULL);
	
	cin >> n;
	for(int i=0; i<n; i++) {
		cin >> num;
		v.push_back(num);
	}
	
	sort(v.begin(),v.end(),greater<int>());
	
	for(int i=0; i<n; i++) {
		num = v[i] - i;
		if(num < 0) {
			ans += 0;
		}
		else {
			ans += num;
		}
	}
	
	cout << ans;
}

