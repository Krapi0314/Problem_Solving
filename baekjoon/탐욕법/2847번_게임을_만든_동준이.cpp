#include <bits/stdc++.h>

using namespace std;

typedef long long ll;
typedef unsigned long long ull;

int n;
int arr[100];
int ans;

int main(void) {
	ios_base::sync_with_stdio(false);
	cin.tie(NULL);
	cout.tie(NULL);

	cin >> n;
	for(int i=0; i<n; i++) {
		cin >> arr[i];
	}
	
	for(int i=n-1; i>=1; i--) {
		if(arr[i-1] >= arr[i]) {
			int diff = arr[i-1] - arr[i];
			ans += diff + 1;
			arr[i-1] -= (diff+1);
		}
	}
	
	cout << ans;
}

