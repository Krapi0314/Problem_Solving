#include <bits/stdc++.h>

using namespace std;

int n;
int a, b, l, p;
int ans;

priority_queue<pair<int, int>,vector<pair<int,int>>,greater<pair<int,int>>> pq;
priority_queue<int> pq2;

int main(void)
{
	ios_base::sync_with_stdio(false);
	cin.tie(NULL);
	cout.tie(NULL);

	cin >> n;
	for (int i = 0; i < n; i++)
	{
		cin >> a >> b;
		pq.push(make_pair(a, b));
	}

	cin >> l >> p;

	while (p < l)
	{
		while (!pq.empty() && p >= pq.top().first)
		{
			pq2.push(pq.top().second);
			pq.pop();
		}

		if (!pq2.empty())
		{
			ans++;
			p += pq2.top(); pq2.pop();
		}
		else
		{
			cout << "-1";
			return 0;
		}
	}

	cout << ans;
}

