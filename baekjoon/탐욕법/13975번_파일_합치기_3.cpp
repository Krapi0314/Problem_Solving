 #include <bits/stdc++.h>

using namespace std;

typedef long long ll;
typedef unsigned long long ull;

int t;
int k;
priority_queue<ull, vector<ull>, greater<ull>> pq;
ull num;
ull ans;

int main(void) {
	ios_base::sync_with_stdio(false);
	cin.tie(NULL);
	cout.tie(NULL);

	cin >> t;
	while(t--) {
		ans = 0;
		
		cin >> k;
		for(int i=0; i<k; i++) {
			cin >> num;
			pq.push(num);
		}
		
		while(pq.size() >= 2) {
			ull n1 = pq.top(); pq.pop();
			ull n2 = pq.top(); pq.pop();
			
			num = n1 + n2;
			
			pq.push(num);
			ans += num;
		}
		pq.pop();
	
		cout << ans << "\n";
	}
}

