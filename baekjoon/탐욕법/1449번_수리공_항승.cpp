#include <bits/stdc++.h>

using namespace std;

typedef long long ll;
typedef unsigned long long ull;

int n, l;
vector<int> v;
int ans;

int main(void) {
	ios_base::sync_with_stdio(false);
	cin.tie(NULL);
	cout.tie(NULL);

	cin >> n >> l;
	for(int i=0; i<n; i++) {
		int num;
		cin >> num;
		v.push_back(num); 
	}
	sort(v.begin(), v.end(), greater<int>());
	
	while(!v.empty()) {
		double left = (double)v.back() - 0.5; v.pop_back();
		double right = left + l;
		
		while(!v.empty() && (double)v.back() + 0.5 <= right) {
			v.pop_back();
		}
		
		ans++;
	}
	
	cout << ans;
}

