#include <bits/stdc++.h>

using namespace std;

typedef long long ll;
typedef unsigned long long ull;

#define MAX 500

int n, m;
int b;
int arr[MAX][MAX];
int temp_time, cnt;
int ans_time, ans_height;

int main(void) {
	ios_base::sync_with_stdio(false);
	cin.tie(NULL);
	cout.tie(NULL);
	
	ans_time = 2e9;
	
	cin >> n >> m >> b;
	for(int i=0; i<n; i++) {
		for(int j=0; j<m; j++) {
			cin >> arr[i][j];
		}
	}
	
	for(int h=256; h>=0; h--) {
		temp_time = 0; cnt = 0;
		for(int i=0; i<n; i++) {
			for(int j=0; j<m; j++) {
				temp_time += (arr[i][j] > h) ? 2*(arr[i][j]-h) : 1*(h-arr[i][j]);
				cnt += (arr[i][j] > h) ? -1*(arr[i][j]-h) : 1*(h-arr[i][j]);
			}
		}
		
		if(cnt <= b) {
			if(temp_time < ans_time) {
				ans_time = temp_time;
				ans_height = h;
			}
		}
	}
	
	cout << ans_time << " " << ans_height;
	return 0;
}

