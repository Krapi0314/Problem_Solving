#include <bits/stdc++.h>

using namespace std;

typedef long long ll;
typedef unsigned long long ull;

int n;
int s,e;
vector<pair<int,int> > v;
int calStack[366];
int h,w;
int ans;

int main(void) {
	ios_base::sync_with_stdio(false);
	cin.tie(NULL);
	cout.tie(NULL);
	
	cin >> n;
	for(int i=0; i<n; i++) {
		cin >> s >> e;
		v.push_back(make_pair(s,e));
	}
	
	sort(v.begin(), v.end());

	for(int i=0; i<v.size(); i++) {
		for(int j=v[i].first; j<=v[i].second; j++) {
			calStack[j]++;
		}
	}
	
	for(int i=1; i<=365; i++) {
		if(calStack[i] == 0) {
			ans += h * w;
			h = 0; w = 0;
		}
		else {
			w++;
			h = max(h, calStack[i]);
		}
	}
	ans += h * w; 
	
	cout << ans;
}

