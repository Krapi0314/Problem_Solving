#include <bits/stdc++.h>

using namespace std;

typedef long long ll;
typedef unsigned long long ull;

#define MAX 10000
int n,m;
int num;
int cnt[MAX+1];
int second;
vector<pair<int,int> > v;

int main(void) {
	ios_base::sync_with_stdio(false);
	cin.tie(NULL);
	cout.tie(NULL);
	
	while(true) {
		second = 0;
		memset(cnt, 0, sizeof(cnt));
		v.clear();
		
		cin >> n >> m;
		
		if(n == 0 && m == 0) break;
		
		for(int i=0; i<n; i++) {
			for(int j=0; j<m; j++) {
				cin >> num;
				
				cnt[num]++;
			}
		}
		
		for(int i=1; i<=MAX; i++) {
			if(cnt[i] >= 1) {
				v.push_back(make_pair(cnt[i], i));
			}
		}
		
		sort(v.begin(), v.end());
		
		second = v[v.size()-2].first;
		
		for(int i=0; i<v.size(); i++) {
			if(v[i].first == second) {
				cout << v[i].second << " ";
			}
		}
		
		cout << "\n";
	}
	
	return 0;
}

