#include <bits/stdc++.h>

using namespace std;

typedef long long ll;
typedef unsigned long long ull;

#define MAX 10000

int n;
int dp[MAX+1]; 

int main(void) {
	ios_base::sync_with_stdio(false);
	cin.tie(NULL);
	cout.tie(NULL);
	
	cin >> n;
	for(int i=1; i<=n; i++) {
		cin >> dp[i];
	}
	
	for(int i=1; i<=n; i++) {
		for(int j=1; j<i; j++) {
			dp[i] = min(dp[i], dp[i-j]+dp[j]);
		}
	}
	
	cout << dp[n];
}

