//#define _CRT_SECURE_NO_WARNINGS

#include <bits/stdc++.h>

using namespace std;

int p[10001] = { 0, };
int d[1001] = { 0, };
int n;

int main(void)
{
	ios_base::sync_with_stdio(false);
	cin.tie(NULL);
	cout.tie(NULL);

	cin >> n;
	for (int i = 1; i <= n; i++)
	{
		cin >> p[i];
	}

	d[0] = 0;
	d[1] = p[1];

	for (int i = 2; i <= n; i++)
	{
		for (int j = 0; j < i; j++)
		{
			d[i] = max(d[i], d[j] + p[i - j]);
		}
	}

	cout << d[n];
}

