#include <bits/stdc++.h>

using namespace std;

typedef long long ll;
typedef unsigned long long ull;

#define MAX 100000

int n;
int dp[MAX+1];

int main(void) {
	ios_base::sync_with_stdio(false);
	cin.tie(NULL);
	cout.tie(NULL);
	
	cin >> n;
	
	for(int i=1; i<=MAX; i++) {
		dp[i] = i;
		
		for(int j=1; j*j<=i; j++) {
			dp[i] = min(dp[i], dp[i-j*j]+1);
		}
	}
	
	cout << dp[n];
}

