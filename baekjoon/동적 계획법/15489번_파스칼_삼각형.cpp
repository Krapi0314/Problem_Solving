#include <bits/stdc++.h>

using namespace std;

typedef long long ll;
typedef unsigned long long ull;

int r,c,w;
int pascal[31][31];
int ans;

int main(void) {
	ios_base::sync_with_stdio(false);
	cin.tie(NULL);
	cout.tie(NULL);
	
	for(int i=1; i<=30; i++) {
		for(int j=1; j<=i; j++) {
			pascal[i][j] = 1;
		}
	}
	
	for(int i=3; i<=30; i++) {
		for(int j=2; j<=i-1; j++) {
			pascal[i][j] = pascal[i-1][j-1] + pascal[i-1][j];
		}
	}
	
	cin >> r >> c >> w;
	
	for(int i=0; i<w; i++) {
		for(int j=0; j<=i; j++) {
			ans += pascal[r+i][c+j];
		}
	}
	
	cout << ans;
}

