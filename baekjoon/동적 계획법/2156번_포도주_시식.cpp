//#define _CRT_SECURE_NO_WARNINGS

#include <bits/stdc++.h>

using namespace std;

int dp[10001];
vector<int> v(1);
int n;
int num;

int main(void)
{
	ios_base::sync_with_stdio(false);
	cin.tie(NULL);
	cout.tie(NULL);

	cin >> n;
	for (int i = 0; i < n; i++)
	{
		cin >> num;
		v.push_back(num);
	}

	dp[0] = 0;
	dp[1] = v[1];
	dp[2] = v[1] + v[2];

	for (int i = 3; i <= n; i++)
	{
		dp[i] = max(dp[i - 3] + v[i - 1] + v[i], dp[i - 2] + v[i]);
		dp[i] = max(dp[i], dp[i - 1]);
	}

	cout << dp[n];
}

