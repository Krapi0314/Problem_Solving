using namespace std;

typedef long long ll;
typedef unsigned long long ull;

#define MAX 1000000
#define MOD 1000000007

int n;
int dp[MAX+1];

int main(void) {
	ios_base::sync_with_stdio(false);
	cin.tie(NULL);
	cout.tie(NULL);
	
	cin >> n;
	
	dp[0] = 0; dp[1] = 1;
	for(int i=2; i<=n; i++) {
		dp[i] = (dp[i-1] % MOD + dp[i-2] % MOD) % MOD;
	}
	
	cout << dp[n];
}

