#include <bits/stdc++.h>

using namespace std;

typedef long long ll;
typedef unsigned long long ull;

ull x,y;
ull l, r, m;
int z, z2;

int main(void) {
	ios_base::sync_with_stdio(false);
	cin.tie(NULL);
	cout.tie(NULL);

	cin >> x >> y;
	z = (100 * y) / x;

	l = 0; r = x;
	while(l <= r) {
		m = (l + r) / 2;
		z2 = (100 * (y+m))/(x+m);
		
		if(z2 - z >= 1) {
			r = m - 1;
		}
		else {
			l = m + 1;
		}
	}
	
	if(l > x) {
		cout << "-1";
	}
	else {
		cout << l;
	}
}

