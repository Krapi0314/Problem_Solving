using namespace std;

typedef long long ll;
typedef unsigned long long ull;

int n;
string s;
stack<char > st;
int ans;

int main(void) {
	ios_base::sync_with_stdio(false);
	cin.tie(NULL);
	cout.tie(NULL);
	
	cin >> n;
	while(n--) {
		while(!st.empty()) st.pop();
		cin >> s;
		
		for(int i=0; i<s.size(); i++) {
			if(!st.empty()) {
				if(st.top() == s[i]) {
					st.pop();
				}
				else {
					st.push(s[i]);
				}
			}
			else {
				st.push(s[i]);
			}
		}
		
		if(st.empty()) {
			ans++;
		}
	}	
	
	cout << ans;
}

