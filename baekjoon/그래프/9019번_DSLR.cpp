#include <bits/stdc++.h>

using namespace std;

typedef long long ll;
typedef unsigned long long ull;

int t;
int a, b;
bool visited[10000];

string bfs(int n, int dest) {
	memset(visited, false, sizeof(visited));
	queue<pair<int, string>> q;
	visited[n] = true;
	q.push(make_pair(n, ""));
	
	while(!q.empty()) {
		int n = q.front().first;
		string s = q.front().second;
		q.pop();
				
		if(n == dest) {
			return s;
		}
		
		// D
		int new_n = (2 * n) % 10000;
		if(!visited[new_n]) {
			visited[new_n] = true;
			q.push(make_pair(new_n, s + "D"));
		}
		// S
		new_n = (n == 0) ? (9999) : (n-1);
		if(!visited[new_n]) {
			visited[new_n] = true;
			q.push(make_pair(new_n, s + "S"));
		}
		// L
		new_n = (n % 1000) * 10 + n / 1000;
		if(!visited[new_n]) {
			visited[new_n] = true;
			q.push(make_pair(new_n, s + "L"));
		}
		// R
		new_n = (n % 10) * 1000 + (n / 10);
		if(!visited[new_n]) {
			visited[new_n] = true;
			q.push(make_pair(new_n, s + "R"));
		}
	}
}

int main(void) {
	ios_base::sync_with_stdio(false);
	cin.tie(NULL);
	cout.tie(NULL);
	
	cin >> t;
	while(t--) {
		cin >> a >> b;
		cout << bfs(a, b) << "\n";
	}
	
	return 0;
}

