//#define _CRT_SECURE_NO_WARNINGS

#include <iostream>

//#include <cstdio>

using namespace std;

int arr[101][101] = { 0, };
int visited[101] = { false, };
int n, m;
int a, b;
int cnt;

void dfs(int start)
{
	visited[start] = 1;
	cnt++;

	for (int i = 1; i <= n; i++)
	{
		if (arr[start][i] == 1 && !visited[i])
		{
			dfs(i);
		}
	}
}

int main(void)
{
	ios_base::sync_with_stdio(false);
	cin.tie(NULL);
	cout.tie(NULL);

	cin >> n;
	cin >> m;
	for (int i = 0; i < m; i++)
	{
		cin >> a >> b;

		arr[a][b] = 1; arr[b][a] = 1;
	}

	dfs(1);

	cout << cnt - 1;
}

